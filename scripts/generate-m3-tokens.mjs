// Generate Material 3 design tokens from the Beakn Deep Teal seed (#0F766E).
//
// Produces:
//   1. design/m3-tokens.json — Material Theme Builder–shaped JSON (full schemes + palettes).
//   2. app/theme.css         — Tailwind v4 @theme block + :root / .dark CSS custom properties.
//
// Re-run with `pnpm m3:generate` after the seed changes (which per HVA-10 it shouldn't).

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

// @material/material-color-utilities 0.4.0 ships broken ESM (missing .js extensions
// on internal imports). Load it via CJS to get a working build.
const require = createRequire(import.meta.url);
const { argbFromHex, hexFromArgb, themeFromSourceColor } = require(
  "@material/material-color-utilities",
);

const SEED = "#0F766E"; // Beakn Deep Teal — locked in HVA-10
const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");

const theme = themeFromSourceColor(argbFromHex(SEED));

// The roles M3 emits on each Scheme that we care about for Tailwind tokens.
// Order matches Material Theme Builder's export ordering.
const ROLES = [
  "primary",
  "onPrimary",
  "primaryContainer",
  "onPrimaryContainer",
  "secondary",
  "onSecondary",
  "secondaryContainer",
  "onSecondaryContainer",
  "tertiary",
  "onTertiary",
  "tertiaryContainer",
  "onTertiaryContainer",
  "error",
  "onError",
  "errorContainer",
  "onErrorContainer",
  "background",
  "onBackground",
  "surface",
  "onSurface",
  "surfaceVariant",
  "onSurfaceVariant",
  "outline",
  "outlineVariant",
  "shadow",
  "scrim",
  "inverseSurface",
  "inverseOnSurface",
  "inversePrimary",
];

function schemeToHexMap(scheme) {
  const out = {};
  for (const role of ROLES) {
    const argb = scheme[role];
    if (typeof argb === "number") out[role] = hexFromArgb(argb);
  }
  return out;
}

const lightHex = schemeToHexMap(theme.schemes.light);
const darkHex = schemeToHexMap(theme.schemes.dark);

// Tonal palette dump (0..100 stops Material Theme Builder ships).
const PALETTE_STOPS = [0, 5, 10, 15, 20, 25, 30, 35, 40, 50, 60, 70, 80, 90, 95, 98, 99, 100];
function paletteToHexMap(palette) {
  const out = {};
  for (const tone of PALETTE_STOPS) out[tone] = hexFromArgb(palette.tone(tone));
  return out;
}

const mtbJson = {
  description: `TYPE: CUSTOM\nBeakn Home Visit App — seed ${SEED} (Deep Teal). Generated from @material/material-color-utilities.`,
  seed: SEED,
  coreColors: { primary: SEED },
  extendedColors: [],
  schemes: {
    light: lightHex,
    dark: darkHex,
  },
  palettes: {
    primary: paletteToHexMap(theme.palettes.primary),
    secondary: paletteToHexMap(theme.palettes.secondary),
    tertiary: paletteToHexMap(theme.palettes.tertiary),
    neutral: paletteToHexMap(theme.palettes.neutral),
    neutralVariant: paletteToHexMap(theme.palettes.neutralVariant),
    error: paletteToHexMap(theme.palettes.error),
  },
};

mkdirSync(resolve(root, "design"), { recursive: true });
writeFileSync(
  resolve(root, "design/m3-tokens.json"),
  JSON.stringify(mtbJson, null, 2) + "\n",
);

// camelCase → kebab-case for CSS custom property names.
const kebab = (s) => s.replace(/[A-Z]/g, (m) => `-${m.toLowerCase()}`);

const cssLightVars = ROLES.map((r) => `  --md-${kebab(r)}: ${lightHex[r]};`).join("\n");
const cssDarkVars = ROLES.map((r) => `  --md-${kebab(r)}: ${darkHex[r]};`).join("\n");

const themeMap = ROLES
  .map((r) => `  --color-${kebab(r)}: var(--md-${kebab(r)});`)
  .join("\n");

const themeCss = `/* AUTO-GENERATED by scripts/generate-m3-tokens.mjs — do not edit by hand.
 * Source: Material 3 dynamic scheme from seed ${SEED} (Deep Teal, locked in HVA-10).
 */

:root {
${cssLightVars}
}

.dark {
${cssDarkVars}
}

@media (prefers-color-scheme: dark) {
  :root:not(.light) {
${cssDarkVars.replace(/^/gm, "  ")}
  }
}

@theme inline {
${themeMap}
}
`;

writeFileSync(resolve(root, "app/theme.css"), themeCss);

console.log(`✓ Wrote design/m3-tokens.json (${Object.keys(lightHex).length} roles × 2 schemes)`);
console.log(`✓ Wrote app/theme.css (Tailwind v4 @theme block + :root/.dark vars)`);
